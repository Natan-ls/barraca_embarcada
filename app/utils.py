import socket

def get_local_ip():
    """Descobre o IP real da TV Box na rede local (Wi-Fi ou Cabo)"""
    try:
        s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
        s.connect(("8.8.8.8", 80))
        ip = s.getsockname()[0]
        s.close()
        return ip
    except Exception:
        return "127.0.0.1"